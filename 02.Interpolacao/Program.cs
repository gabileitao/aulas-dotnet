using System;

namespace _02.Interpolacao {
    class Program {
        static void Main(string[] args) {
            float a = 2;
            float b = 100;

            Console.WriteLine($"A soma de {a} e {b} é {a + b}.");
            Console.WriteLine($"A sub de {a} e {b} é {a - b}.");
            Console.WriteLine($"A mult de {a} e {b} é {a * b}.");
            Console.WriteLine($"A div de {a} e {b} é {a / b}.");
        }
    }
}
