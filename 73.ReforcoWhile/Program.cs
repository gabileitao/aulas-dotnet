using System;

namespace _73.ReforcoWhile {
    class Program {
        static void Main(string[] args) {

            
            for (int i = 0; i <= 10; i++) {
                Console.WriteLine(i);
                
            }

            int j = 0;
            while(j <= 10) {
                Console.WriteLine(j);
                j++;
            }
        }
    }
}
